# string indexing
name = "sahil"
print(name[0])
print(name[1])
print(name[2])
print(name[3])
print(name[4])
  
  
# string slicing
name = "sahil"
print(name[0:3])  # will print the character with index number 0,1 and 2 but will exclude index number 3
print(name[:3])   # this is also same as above line because if we keep the first place blank it is assumed as 0
print(name[0:])   # this is assumed as [0:4]


