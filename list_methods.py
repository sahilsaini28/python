# list methods
list = [1,5,7,9,6]

list.sort()  #sorts the list
print(list)

list.reverse()  #reverse the order of the list
print(list)

list.append(10)  #adds elements at the end of the list
print(list)

list.insert(1,100)  #adds 100 at index 1
print(list)

list.pop(2)  #remove element from index 2
print(list)

list.remove(7)  #remove element form the list
print(list)